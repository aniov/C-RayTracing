#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	size_t i;

	i = ft_strlen(dest);
	while (*src != '\0')
	{
		dest[i] = *src++;
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
