#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	size_t	i;
	char	*str;

	str = 0;
	if (s == 0 || f == 0)
		return (0);
	i = 0;
	str = (char*)malloc((ft_strlen(s) + 1) * sizeof(char));
	if (str == 0)
		return (0);
	while (s[i] != '\0')
	{
		str[i] = (*f)(s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
