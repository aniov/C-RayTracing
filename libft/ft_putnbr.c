#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int nr;

	if (n < 0)
	{
		write(1, "-", 1);
		nr = -n;
	}
	else
		nr = n;
	if (nr >= 10)
	{
		ft_putnbr(nr / 10);
		ft_putchar(nr % 10 + '0');
	}
	if (nr < 10)
		ft_putchar(nr + '0');
}
