#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t i;

	i = ft_strlen(dest);
	while (*src != '\0' && n > 0)
	{
		dest[i] = *src++;
		i++;
		n--;
	}
	if (n > 0)
		dest[i] = '\0';
	return (dest);
}
