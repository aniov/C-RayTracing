#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	i;

	if (s == 0 || f == 0)
		return (0);
	str = 0;
	i = 0;
	str = (char*)malloc((ft_strlen(s) + 1) * sizeof(char));
	if (str == 0)
		return (0);
	while (s[i] != '\0')
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
